import turtle

vansh = turtle.Turtle(visible=False)
vansh.color("red", "orange")
# Square
vansh.begin_fill()
vansh.fd(100)
vansh.left(90)
vansh.fd(100)
vansh.left(90)
vansh.fd(100)
vansh.left(90)
vansh.fd(100)


vansh.penup()
vansh.fd(100)
vansh.pendown()

vansh.fd(100)
vansh.lt(90)
vansh.fd(100)
vansh.lt(90)
vansh.fd(100)
vansh.lt(90)
vansh.fd(100)

vansh.end_fill()
# Square 2
turtle.done()
